(EMP_ID INT PRIMARY KEY UNIQUE, EMP_NAME VARCHAR(255) NOT NULL, EMP_ROLE VARCHAR(255));

CREATE TABLE IN_EMP_DETAILS
(EMP_ID INT PRIMARY KEY UNIQUE, EMP_NAME VARCHAR(255) NOT NULL, EMP_ROLE VARCHAR(255));

