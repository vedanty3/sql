-- INTERSECT
SELECT USA.* FROM USA_EMP_DETAILS AS USA INNER JOIN IN_EMP_DETAILS AS IND USING(EMP_ID);

-- MINUS
SELECT USA.* FROM USA_EMP_DETAILS AS USA LEFT JOIN IN_EMP_DETAILS AS IND 
USING(EMP_ID) WHERE IND.EMP_ID IS NULL;

